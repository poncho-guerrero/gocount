const axios = require('axios');

/**
 * @class
 * 
 * Contains the modal sign in form.
 * @name SignIn
 * 
 * @requires NPM:axios
 */
class SignOut {
  post() {
    axios
      .post('/server/signout', {})
      .then(function(res) {
        location.reload();
      });
  }
}

module.exports = SignOut;